<p>开篇的“弩机猜想”特别有穿透力：弩机之所以重要，不只是射程与杀伤，而是它让统治者有机会把原先动员不动的平民，变成可驱策的大规模部队；而这又以打破血缘小圈子、建立科层化管理为前提。</p>

